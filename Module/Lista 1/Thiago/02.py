# Leia dois valores inteiros a e b informados pelo usuário e determine o
# valor de a * b. O cálculo de a * b deve ser realizado por uma função
# que recebe estes dois valores inteiros como parâmetro.

def mult(a, b):
    return a * b


n1 = int(input())
n2 = int(input())

mult = mult(n1, n2)

print(mult)
